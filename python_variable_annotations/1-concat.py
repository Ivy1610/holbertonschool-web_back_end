#!/usr/bin/env python3
"""
1-concat.py

This module contains a type-annotated function `concat`
that concatenates two strings
"""


def concat(str1: str, str2: str) -> str:
    """
    Concatenates stwo string and returns the result

    Args:
        str1 (str): First string
        str2 (str): Second string

    Returns:
        str: Concatenated string
    """
    return str1 + str2

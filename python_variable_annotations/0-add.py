#!/usr/bin/env python3
"""
0-add.py

This module contains a type-annotated function
`add` that sums two floats
"""


def add(a: float, b: float) -> float:
    """
    Returns the sum of two float numbers

    Args:
        a (float): First number
        b (float): Secon number

    Returns:
        float: Sum of a and b
    """
    return a + b

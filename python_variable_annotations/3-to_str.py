#!/usr/bin/env python3
"""
type-annotated function to_str that takes a float n as
argument and returns the string representation of the float.
"""


def to_str(n: float) -> str:
    """
    Return the string representation of a float

    Args:
        n (float): The float number

    Returns:
        str: The string representation of the float
    """
    return str(n)

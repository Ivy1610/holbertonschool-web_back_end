#!/usr/bin/env python3
"""
3-to_str.py

This module contains a type-annotated function `to_str`
that convers a float to string
"""


def to_str(n: float) -> str:
    """
    Converts a float to its string representation

    Args:
        n (float): Float number

    Returns:
        str: String representation of n
    """
    return str(n)
